    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nome = pegarNome.getText();
        int pessoas = Integer.parseInt(pegarPessoas.getText());

        if (pessoas == 1 || pessoas == 3
                || pessoas == 5 || pessoas > 6 || pessoas < 1) {

            JOptionPane.showMessageDialog(null, "Tipo de mesa nÃ£o disponivel.", null, JOptionPane.INFORMATION_MESSAGE);

        } else {
            
        CadastroVO cadastro = new CadastroVO(nome, pessoas);
        RestauranteBO.reservar(cadastro);
        pegarNome.setText("");
        pegarPessoas.setText("");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

